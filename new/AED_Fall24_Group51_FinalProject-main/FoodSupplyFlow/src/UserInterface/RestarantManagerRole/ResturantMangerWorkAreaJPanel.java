    private void showProductBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showProductBtnActionPerformed
        // TODO add your handling code here:

    int selectedRow = orderWorkRequestTable.getSelectedRow();
    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(null, "Please select a request");
        return;
    }

    Object selectedObject = orderWorkRequestTable.getValueAt(selectedRow, 0);
    if (selectedObject instanceof FoodRequirementRequest) {
        FoodRequirementRequest selectedRequest = (FoodRequirementRequest) selectedObject;

        DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();
        model.setRowCount(0); // Clear the table

        ArrayList<Products> productList = selectedRequest.getProductList();
        if (productList != null && !productList.isEmpty()) {
            for (Products p : productList) {
                Object[] row = new Object[3];
                row[0] = p; // Assuming toString() provides Food Type
                row[1] = p.getProductName();
                row[2] = p.getQuantity();
                model.addRow(row);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No products found for this request.");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Invalid selection. Please select a valid request.");
    }      
    }//GEN-LAST:event_showProductBtnActionPerformed

