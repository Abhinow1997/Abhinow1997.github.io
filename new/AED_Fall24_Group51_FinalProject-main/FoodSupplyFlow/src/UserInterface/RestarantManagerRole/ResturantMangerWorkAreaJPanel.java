    private void populateRequestTable() {
    DefaultTableModel model = (DefaultTableModel) orderWorkRequestTable.getModel();
    model.setRowCount(0); 

    for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()) {
        if (request instanceof FoodRequirementRequest) {
                Object[] row = new Object[4];
                row[0] = request;
                row[1] = ((FoodRequirementRequest) request).getProductList();
                row[2] = request.getReceiver() != null ? request.getReceiver().getUsername() : "Unassigned";
                row[3] = request.getStatus();
                model.addRow(row);
        }
    }
}

